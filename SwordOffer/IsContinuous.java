package com.algorithm.offer;
/**
 * LL今天心情特别好,因为他去买了一副扑克牌,发现里面居然有2个大王,2个小王(一副牌原本是54张^_^)...
 * 他随机从中抽出了5张牌,想测测自己的手气,看看能不能抽到顺子,如果抽到的话,他决定去买体育彩票,嘿嘿！！
 * “红心A,黑桃3,小王,大王,方片5”,“Oh My God!”不是顺子.....LL不高兴了,他想了想,
 * 决定大\小 王可以看成任何数字,并且A看作1,J为11,Q为12,K为13。
 * 上面的5张牌就可以变成“1,2,3,4,5”(大小王分别看作2和4),“So Lucky!”。
 * LL决定去买体育彩票啦。 现在,要求你使用这幅牌模拟上面的过程,然后告诉我们LL的运气如何。
 * 为了方便起见,你可以认为大小王是0。
 * @author Zhu
 * 思路：可以找出最大的数和最小的数，然后求差，如果差值大于4的话肯定就不是顺子，还有如果有重复的数字那肯定也不是顺子
 */
public class IsContinuous {
	 public static boolean isContinuous(int [] numbers){
		 if(numbers==null||numbers.length<5) {
			 return false;
		 }
		 int len=5;
		 int max=-1,min=15;
		 int[] mark=new int[14];
		 for(int i=0;i<len;i++) {
			 if(++mark[numbers[i]]>1&&numbers[i]!=0) {
				 return false;
			 }
			 if(numbers[i]<min&&numbers[i]!=0) {
				 min=numbers[i];
			 }
			 if(numbers[i]>max) {
				 max=numbers[i];
			 }
		 }
		 return max-min>=5?false:true;
	 }
	 public static void main(String[] args) {
		int [] numbers= {1,3,5,0,0};
		System.out.println(isContinuous(numbers));
	}
}
